using System;

namespace Calculator
{
    public class RegularCalculator
    {
        public double Addition(double a, double b)
        {
            return a + b;
        }

        public double Subsctraction(double a, double b)
        {
            return a - b;
        }

        public double Division(double a, double b)
        {
            return a / b;
        }

        public double Multiplictation(double a, double b)
        {
            return a * b;
        }
    }
}
